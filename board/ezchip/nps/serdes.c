int nps_dbg_lan_serdes_init(void)
{
	int ret_val;

	get_eth_config();
	ret_val = serdes_upload_ucode();
	if (ret_val) {
		printf("serdes upload ucode failed!\n");
		return 0;
	}

	ret_val = serdes_power_up();
	if (ret_val) {
		printf("serdes power up failed!\n");
		return 0;
	}

	return 1;
}
